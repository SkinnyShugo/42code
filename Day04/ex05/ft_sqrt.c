#include <unistd.h>

int	ft_sqrt(int nb)
{
	int i;
	int sq;

	i = 0;
	if (nb < 0)
		return (0);
	else
	{
		while (i * i != nb)
		{
			i++;
		}
	}
	return (i);
}
